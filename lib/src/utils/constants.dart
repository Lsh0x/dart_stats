import 'dart:math' as math;

/// Pi (π ≈ 3.14159265358979...)
const double pi = math.pi;

/// Euler's number (e ≈ 2.71828182845904...)
const double e = math.e;

/// Square root of 2 (√2 ≈ 1.41421356237310...)
const double sqrt2 = math.sqrt2;

/// Square root of π (√π ≈ 1.77245385090552...)
final double sqrtPi = math.sqrt(math.pi);

/// Square root of 2π (√(2π) ≈ 2.50662827463100...)
final double sqrt2Pi = math.sqrt(2 * math.pi);

/// 1 / √(2π) ≈ 0.39894228040143...
final double invSqrt2Pi = 1.0 / math.sqrt(2 * math.pi);

/// ln(2π) ≈ 1.83787706640935...
final double ln2Pi = math.log(2 * math.pi);

/// Natural logarithm of 2 (ln(2) ≈ 0.69314718055995...)
const double ln2 = math.ln2;
